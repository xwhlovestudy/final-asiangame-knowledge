package com.xh229050532.Asian_Game_Knowledge;

public class TiBean {
    private int id;
    private String ti;
    private String info;
    private String []daan;
    private int dui;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getTi() {
        return ti;
    }

    public void setTi(String ti) {
        this.ti = ti;
    }

    public String[] getDaan() {
        return daan;
    }

    public void setDaan(String[] daan) {
        this.daan = daan;
    }

    public int getDui() {
        return dui;
    }

    public void setDui(int dui) {
        this.dui = dui;
    }
}
